package exercicios;

public class Exercicio18 {
	 public static double ConverterCelsiusParaFahrenheit(double celsius) {
	        return (celsius * 9 / 5) + 32;
	    }

	    public static void main(String[] args) {
	      
	        System.out.println(ConverterCelsiusParaFahrenheit(25));  
	    }
	}

